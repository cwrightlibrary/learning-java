// Class definition (the name of the class has to match
// the name of the file it's in "ChapterOne.java")
public class ChapterOne {
    // Method definition (main is the method)
    public static void main(String[] args) {
        // Statement performs a basic operation
        // Note that Java is case-sensitive
        System.out.println("Hello, World!");
        // System.out.println displays results, println
        // stands for "print line"

        System.out.println("How are you?");
        // Can print multiple lines

        // Printing without a newline
        System.out.print("Goodbye, ");
        System.out.println("cruel world");

        // Using typical escape sequences
        System.out.print("Hello!\nHow are you doing?\n");

        // This maintains the quotes around "Hello!"
        System.out.println("She said \"Hello!\" to me.");

        // List of more:
        // \n - newline
        // \t - tab
        // \" double quote
        // \\ backslash
    }
}
